#include "main.h"

/**
 * add - accessory function
 *
 * Description: returns the result of the addition of two parameters a and b
 *
 * @a: The first parameter to be added
 *
 * @b: The second parameter to be added
 *
 * Return: Always void (success)
 */

int add(int a, int b)
{
	int output;

	output = a + b;

	return (output);
}
